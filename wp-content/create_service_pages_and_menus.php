function hws_page_content($title, $lead, $sections, $cta = true) {
    $html = '<div class="hws-service-page">';
    $html .= '<p class="hws-service-lead">' . esc_html($lead) . '</p>';
    foreach ($sections as $section) {
        $html .= '<section class="hws-service-section">';
        $html .= '<h2>' . esc_html($section['title']) . '</h2>';
        if (!empty($section['text'])) {
            foreach ((array) $section['text'] as $paragraph) {
                $html .= '<p>' . wp_kses_post($paragraph) . '</p>';
            }
        }
        if (!empty($section['items'])) {
            $html .= '<ul>';
            foreach ($section['items'] as $item) {
                $html .= '<li>' . wp_kses_post($item) . '</li>';
            }
            $html .= '</ul>';
        }
        $html .= '</section>';
    }
    if ($cta) {
        $html .= '<div class="hws-service-cta">';
        $html .= '<div><strong>Нужна помощь с подбором?</strong><br>Отправьте параметры парной, фото объекта или ссылку на товар. Менеджер поможет выбрать модель, комплектацию и маршрут поставки.</div>';
        $html .= '<p><a class="button" href="https://t.me/" rel="nofollow">Написать в Telegram</a> <a class="button" href="https://wa.me/" rel="nofollow">Написать в WhatsApp</a></p>';
        $html .= '</div>';
    }
    $html .= '</div>';
    return $html;
}

