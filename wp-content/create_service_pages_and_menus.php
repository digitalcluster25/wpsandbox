function hws_nav_widget_id($title, $menu_id) {
    $widgets = get_option('widget_nav_menu', []);
    if (!is_array($widgets)) {
        $widgets = [];
    }
    foreach ($widgets as $id => $widget) {
        if (is_numeric($id) && isset($widget['title']) && $widget['title'] === $title) {
            $widgets[$id] = ['title' => $title, 'nav_menu' => $menu_id];
            update_option('widget_nav_menu', $widgets);
            return (int) $id;
        }
    }
    $id = 2;
    while (isset($widgets[$id])) {
        $id++;
    }
    $widgets[$id] = ['title' => $title, 'nav_menu' => $menu_id];
    $widgets['_multiwidget'] = 1;
    update_option('widget_nav_menu', $widgets);
    return $id;
}

