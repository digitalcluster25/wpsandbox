        'post_name' => $data['slug'],
        'post_content' => hws_page_content($data['title'], $data['lead'], $data['sections']),
        'post_status' => 'publish',
        'post_type' => 'page',
    ];
    if ($existing) {
        $postarr['ID'] = $existing->ID;
        $id = wp_update_post($postarr, true);
    } else {
        $id = wp_insert_post($postarr, true);
    }
    if (is_wp_error($id)) {
        throw new RuntimeException($id->get_error_message());
    }
    update_post_meta($id, '_hws_service_page', '1');
    return (int) $id;
}

$page_ids = [];
